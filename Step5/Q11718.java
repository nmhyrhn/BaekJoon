package BaekJoon.Step5;

import java.util.Scanner;

public class Q11718 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String str;

        while(sc.hasNextLine()) {
            //haeNextLine은 다음 입력값이 있는지 확인해줌
            str = sc.nextLine();
            System.out.println(str);
        }
    }
}
